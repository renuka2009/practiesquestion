 function AreaRectangle (base,height)
 {
     return base*height
 }

 console.log(AreaRectangle(24,5))

 function AreaSquare (side)
 {
     return side*side
 }

 console.log(AreaSquare(30))


 function AreaTriangle ( base,height)
 {
     return 1/2*base*height
 }

 console.log(AreaTriangle(30,5))

